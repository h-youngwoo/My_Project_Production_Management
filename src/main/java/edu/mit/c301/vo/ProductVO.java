package edu.mit.c301.vo;

public class ProductVO {
	
	private String product_code;
	private String product_name;
	private int manufacturing_cost;
	private int goal_quantity;
	private int stock;
	private int selling_cost;
	private String group_code;
	private String group_name;
	
	public String getProduct_code() {
		return product_code;
	}
	public void setProduct_code(String product_code) {
		this.product_code = product_code;
	}
	public String getProduct_name() {
		return product_name;
	}
	public void setProduct_name(String product_name) {
		this.product_name = product_name;
	}
	public int getManufacturing_cost() {
		return manufacturing_cost;
	}
	public void setManufacturing_cost(int manufacturing_cost) {
		this.manufacturing_cost = manufacturing_cost;
	}
	public int getGoal_quantity() {
		return goal_quantity;
	}
	public void setGoal_quantity(int goal_quantity) {
		this.goal_quantity = goal_quantity;
	}
	public int getStock() {
		return stock;
	}
	public void setStock(int stock) {
		this.stock = stock;
	}
	public int getSelling_cost() {
		return selling_cost;
	}
	public void setSelling_cost(int selling_cost) {
		this.selling_cost = selling_cost;
	}
	public String getGroup_code() {
		return group_code;
	}
	public void setGroup_code(String group_code) {
		this.group_code = group_code;
	}
	public String getGroup_name() {
		return group_name;
	}
	public void setGroup_name(String group_name) {
		this.group_name = group_name;
	}
	

	

}
